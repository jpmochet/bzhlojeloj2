export function getListingById(id: string): Listing | undefined {
  return listings.find((l) => l.id === id)
}

export function getListingsByCity(city: string): Listing[] {
  return listings.filter((l) => l.city.toLowerCase() === city.toLowerCase())
}

export function getEligibleAnnualListings(): Listing[] {
  return listings.filter(
    (l) => l.annualEligibilityStatus === 'ELIGIBLE_ANNUAL' || 
           l.annualEligibilityStatus === 'ANNUAL_PREBOOKED' ||
           l.annualEligibilityStatus === 'ANNUAL_ACTIVE'
  )
}
